package Session6;

public class Date {
    public int year,month, day;

    public Date(int year,int month,int day){
        this.year = year;
        this.month = month;
        this.day = day;
    }
    public int getYear()
    {
        return this.year;
    }
    public int getMonth()
    {
        return this.month;
    }public int getDay()
    {
        return this.day;
    }public int setYear(int year)
    {
        return this.year;
    }
    public int setMonth(int month)
    {
        return this.month = month;
    }
    public int setDay(int day)
    {
        return this.day;
    }
}
